package myCollection.model;

public class UpdateCollectionsException extends Exception {

	public UpdateCollectionsException() {
	}

	public UpdateCollectionsException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UpdateCollectionsException(String message, Throwable cause) {
		super(message, cause);
	}

	public UpdateCollectionsException(String message) {
		super(message);
	}

	public UpdateCollectionsException(Throwable cause) {
		super(cause);
	}

}
